# Розмістити елементи непарних рядків у апорядку спадання.


A = [
    [9, 5, 3, 0],
    [0, -7, 1, 4],
    [5, 0, 8, 3],
    [0, -3, 5, 1]
]
# Створюємо відсортований список списків
lis = []
for el in A[::2]:
    B = [sorted(el, reverse=True)]
    lis += B
    # Замінюємо списки на відсортовані
A[::2] = lis
for i in A:
    print(i)







