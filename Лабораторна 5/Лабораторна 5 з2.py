# Дано Nn . Побудувати алгоритм для знаходження числа m , у якому цифри йдуть у
# зворотному порядку (наприклад, якщо 2713 n , то 3172m


n = int(input("Введіть натуральне число: "))
m = 0

while n > 0:
    digit = n % 10 ;
    n = n // 10;
    m = m * 10
    m = m + digit

print('"Зворотнє число:', m)
